@dataclass(frozen=True)
class PlaytestCase:
    case_id: str
    title: str
    mode: str
    map_name: str
    bot_target: int
    duration_minutes: int
    cvars: tuple[tuple[str, str], ...]
    profiles: tuple[str, ...]
    focus: tuple[str, ...]
    observations: tuple[str, ...]
    pass_criteria: tuple[str, ...]
    failure_signals: tuple[str, ...]

    @property
    def config_name(self) -> str:
        return f"bot_playtest_{self.case_id}.cfg"

    def all_cvars(self) -> tuple[tuple[str, str], ...]:
        reset_cvars = tuple((name, "0") for name in MODE_POLICY_CVARS)
        return (*COMMON_CVARS, *reset_cvars, *self.cvars)

    def command_lines(self) -> tuple[str, ...]:
        return (
            *(f"set {name} {config_value(value)}" for name, value in self.all_cvars()),
            f"map {self.map_name}",
            "botlist",
        )

    def config_text(self) -> str:
        lines = [
            f"// Generated by tools/bot_playtest/generate_bot_playtest.py",
            f"// Case: {self.case_id} ({self.mode})",
            f"// Goal: {self.title}",
            "kickbots",
            *self.command_lines(),
            "",
            "// After bots spawn, run: botlist",
            "// Watch for the failure signals listed in the generated checklist.",
        ]
        return "\n".join(lines) + "\n"

    def to_json(self) -> dict[str, Any]:
        return {
            "id": self.case_id,
            "title": self.title,
            "mode": self.mode,
            "map": self.map_name,
            "bot_target": self.bot_target,
            "duration_minutes": self.duration_minutes,
            "config": self.config_name,
            "profiles": list(self.profiles),
            "cvars": [{"name": name, "value": value} for name, value in self.all_cvars()],
            "commands": list(self.command_lines()),
            "focus": list(self.focus),
            "observations": list(self.observations),
            "pass_criteria": list(self.pass_criteria),
            "failure_signals": list(self.failure_signals),
        }


def config_value(value: str) -> str:
    if value == "":
        return '""'
    if any(ch.isspace() for ch in value):
        escaped = value.replace('"', r'\"')
        return f'"{escaped}"'
    return value


