def validate_cases(cases: Iterable[PlaytestCase]) -> list[str]:
    failures: list[str] = []
    case_list = list(cases)
    modes = {case.mode for case in case_list}
    for mode in REQUIRED_MODES:
        if mode not in modes:
            failures.append(f"missing required playtest mode: {mode}")

    ids: set[str] = set()
    for case in case_list:
        if case.case_id in ids:
            failures.append(f"duplicate playtest case id: {case.case_id}")
        ids.add(case.case_id)
        if case.bot_target <= 0:
            failures.append(f"{case.case_id} must request at least one bot")
        if not case.pass_criteria:
            failures.append(f"{case.case_id} has no pass criteria")
        if not case.failure_signals:
            failures.append(f"{case.case_id} has no failure signals")
        text = case.config_text()
        for token in FORBIDDEN_PUBLIC_TOKENS:
            if token in text:
                failures.append(f"{case.case_id} generated forbidden public token: {token}")
    return failures


def now_utc() -> str:
    return dt.datetime.now(dt.timezone.utc).isoformat(timespec="seconds").replace("+00:00", "Z")


def rel(path: pathlib.Path, repo_root: pathlib.Path) -> str:
    try:
        return path.resolve().relative_to(repo_root.resolve()).as_posix()
    except ValueError:
        return str(path)


def render_markdown(
    cases: Iterable[PlaytestCase],
    *,
    repo_root: pathlib.Path,
    output_dir: pathlib.Path,
    base_game: str,
    install_dir: pathlib.Path,
    generated_at: str,
) -> str:
    case_list = list(cases)
    total_minutes = sum(case.duration_minutes for case in case_list)
    executable = ".\\worr_ded_x86_64.exe"
    install_game_dir = install_dir / base_game
    generated_inside_game_dir = output_dir.resolve() == install_game_dir.resolve()

    lines = [
        "# WORR Bot Multiplayer Playtest Checklist",
        "",
        f"Generated: `{generated_at}`",
        f"Cases: `{len(case_list)}`",
        f"Estimated active time: `{total_minutes} minutes`",
        "",
        "## Before You Start",
        "",
        "- Use a fresh staged build under `.install/` when validating release candidates.",
        "- Generate executable configs into `.install/basew` if you want to run them with `+exec`.",
        "- Keep the console open and use `botlist` after each map starts.",
        "- Treat spinning in place, wall-sticking, point-blank overlap, and blaster-only suicide pushes as failures to investigate.",
        "",
        "## Generate Configs",
        "",
        "```powershell",
        "python tools\\bot_playtest\\generate_bot_playtest.py --output-dir .tmp\\bot_playtest",
        "python tools\\bot_playtest\\generate_bot_playtest.py --output-dir .install\\basew",
        "```",
        "",
    ]

    for index, case in enumerate(case_list, start=1):
        lines.extend(
            [
                f"## {index}. {case.title}",
                "",
                f"- Case: `{case.case_id}`",
                f"- Mode: `{case.mode}`",
                f"- Map: `{case.map_name}`",
                f"- Bot target: `{case.bot_target}`",
                f"- Duration: `{case.duration_minutes} minutes`",
                f"- Profiles: `{', '.join(case.profiles)}`",
                "",
                "Focus:",
            ]
        )
        lines.extend(f"- {item}" for item in case.focus)
        lines.extend(["", "Run:"])
        if generated_inside_game_dir:
            lines.extend(
                [
                    "",
                    "```powershell",
                    f"cd {rel(install_dir, repo_root)}",
                    f"{executable} +set basedir . +set game {base_game} +exec {case.config_name}",
                    "```",
                ]
            )
        else:
            lines.extend(
                [
                    "",
                    "```text",
                    *(case.command_lines()),
                    "```",
                ]
            )
        lines.extend(["", "Observe:"])
        lines.extend(f"- {item}" for item in case.observations)
        lines.extend(["", "Pass criteria:"])
        lines.extend(f"- {item}" for item in case.pass_criteria)
        lines.extend(["", "Failure signals:"])
        lines.extend(f"- {item}" for item in case.failure_signals)
        lines.append("")

    lines.extend(
        [
            "## Wrap-Up",
            "",
            "- Save the generated JSON artifact with the release or bug report notes.",
            "- Record the exact map, profile mix, bot count, and any visible failure signal.",
            "- Re-run `tools/bot_release/run_bot_acceptance.py` after updating bot behavior or staged assets.",
            "",
        ]
    )
    return "\n".join(lines)


def build_payload(
    cases: Iterable[PlaytestCase],
    *,
    repo_root: pathlib.Path,
    output_dir: pathlib.Path,
    base_game: str,
    generated_at: str,
) -> dict[str, Any]:
    case_list = list(cases)
    return {
        "schema": SCHEMA,
        "generated_at": generated_at,
        "repo_root": str(repo_root.resolve()),
        "output_dir": str(output_dir.resolve()),
        "base_game": base_game,
        "summary": {
            "cases": len(case_list),
            "modes": sorted({case.mode for case in case_list}),
            "total_minutes": sum(case.duration_minutes for case in case_list),
            "bot_profiles": list(BOT_PROFILES),
        },
        "cases": [case.to_json() for case in case_list],
    }


def build_notes_template(
    cases: Iterable[PlaytestCase],
    *,
    playtest_artifact: str,
    generated_at: str,
) -> dict[str, Any]:
    return {
        "schema": NOTES_SCHEMA,
        "generated_at": generated_at,
        "playtest_artifact": playtest_artifact,
        "operator": "",
        "build": "",
        "server": "",
        "notes": "",
        "cases": [
            {
                "id": case.case_id,
                "outcome": "pending",
                "duration_minutes": case.duration_minutes,
                "botlist": "",
                "profiles_observed": [],
                "failure_signals": [],
                "custom_failure_signals": [],
                "repro_steps": [],
                "notes": "",
            }
            for case in cases
        ],
    }


def generate_outputs(
    *,
    repo_root: pathlib.Path = REPO_ROOT,
    output_dir: pathlib.Path = DEFAULT_OUTPUT_DIR,
    base_game: str = "basew",
    install_dir: pathlib.Path | None = None,
) -> dict[str, Any]:
    repo_root = repo_root.resolve()
    output_dir = (repo_root / output_dir).resolve() if not output_dir.is_absolute() else output_dir.resolve()
    install_dir = (install_dir or repo_root / ".install").resolve()
    cases = default_playtest_cases()
    failures = validate_cases(cases)
    if failures:
        raise SystemExit("\n".join(failures))

    output_dir.mkdir(parents=True, exist_ok=True)
    generated_at = now_utc()

    config_paths: list[pathlib.Path] = []
    for case in cases:
        path = output_dir / case.config_name
        path.write_text(case.config_text(), encoding="utf-8", newline="\n")
        config_paths.append(path)

    markdown_path = output_dir / "bot_multiplayer_playtest.md"
    json_path = output_dir / "bot_multiplayer_playtest.json"
    notes_template_path = output_dir / "bot_multiplayer_playtest_notes_template.json"
    markdown = render_markdown(
        cases,
        repo_root=repo_root,
        output_dir=output_dir,
        base_game=base_game,
        install_dir=install_dir,
        generated_at=generated_at,
    )
    markdown_path.write_text(markdown, encoding="utf-8", newline="\n")

    payload = build_payload(
        cases,
        repo_root=repo_root,
        output_dir=output_dir,
        base_game=base_game,
        generated_at=generated_at,
    )
    payload["artifacts"] = {
        "markdown": rel(markdown_path, repo_root),
        "json": rel(json_path, repo_root),
        "notes_template": rel(notes_template_path, repo_root),
        "configs": [rel(path, repo_root) for path in config_paths],
    }
    json_path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8", newline="\n")

    notes_template = build_notes_template(
        cases,
        playtest_artifact=rel(json_path, repo_root),
        generated_at=generated_at,
    )
    notes_template_path.write_text(
        json.dumps(notes_template, indent=2) + "\n",
        encoding="utf-8",
        newline="\n",
    )
    return payload


def print_summary(payload: dict[str, Any]) -> None:
    summary = payload["summary"]
    print(
        "generated "
        f"{summary['cases']} bot playtest cases for {', '.join(summary['modes'])} "
        f"({summary['total_minutes']} minutes)"
    )
    artifacts = payload["artifacts"]
    print(f"markdown: {artifacts['markdown']}")
    print(f"json: {artifacts['json']}")
    print(f"notes_template: {artifacts['notes_template']}")
    for path in artifacts["configs"]:
        print(f"config: {path}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--repo-root",
        type=pathlib.Path,
        default=REPO_ROOT,
        help="repository root; defaults to the script's repository",
    )
    parser.add_argument(
        "--output-dir",
        type=pathlib.Path,
        default=DEFAULT_OUTPUT_DIR,
        help="directory for generated markdown, JSON, and cfg files",
    )
    parser.add_argument(
        "--base-game",
        default="basew",
        help="game directory name used in generated launch examples",
    )
    parser.add_argument(
        "--install-dir",
        type=pathlib.Path,
        default=None,
        help="staged install root used in launch examples; defaults to .install",
    )
    parser.add_argument(
        "--format",
        choices=("text", "json"),
        default="text",
        help="stdout format",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    payload = generate_outputs(
        repo_root=args.repo_root,
        output_dir=args.output_dir,
        base_game=args.base_game,
        install_dir=args.install_dir,
    )
    if args.format == "json":
        print(json.dumps(payload, indent=2))
    else:
        print_summary(payload)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
