static uint64_t hash_u32(uint64_t hash, uint32_t value) {
  unsigned int index;
  for (index = 0; index != 4; ++index) {
    hash ^= (uint8_t)(value & UINT32_C(0xff));
    hash *= UINT64_C(1099511628211);
    value >>= 8;
  }
  return hash;
}

bool Worr_LocalActionWeaponSemanticsDigestV1(uint64_t *digest_out) {
  uint64_t digest = UINT64_C(1469598103934665603);
  uint32_t catalog_id;

  if (!digest_out || *digest_out != 0)
    return false;
  digest = hash_u32(digest, UINT32_C(0x4c415331)); /* LAS1 */
  digest = hash_u32(digest, WORR_LOCAL_ACTION_WEAPON_SEMANTICS_ABI_VERSION);
  digest = hash_u32(digest, WORR_LOCAL_ACTION_WEAPON_SEMANTICS_MODEL_REVISION);
  digest = hash_u32(digest, WORR_LOCAL_ACTION_CATALOG_COUNT);
  for (catalog_id = 1; catalog_id <= WORR_LOCAL_ACTION_CATALOG_COUNT;
       ++catalog_id) {
    const uint8_t *bytes = (const uint8_t *)&semantics[catalog_id - 1];
    size_t index;
    if (!Worr_LocalActionWeaponSemanticsValidateV1(
            &semantics[catalog_id - 1])) {
      return false;
    }
    for (index = 3; index < sizeof(semantics[0]) / sizeof(uint32_t); ++index) {
      uint32_t word;
      memcpy(&word, bytes + index * sizeof(word), sizeof(word));
      digest = hash_u32(digest, word);
    }
  }
  *digest_out = digest;
  return true;
}

