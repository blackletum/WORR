static bool output_is_zero(const void *output, size_t size) {
  const uint8_t *bytes = (const uint8_t *)output;
  size_t index;

  if (!output)
    return false;
  for (index = 0; index != size; ++index) {
    if (bytes[index] != 0)
      return false;
  }
  return true;
}

